int	ft_count_if(char **tab, int (*f)(char*))
{
	int length;
	int i;

	length = 0;
	i = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			length++;
		i++;
	}
	return (length);
}
