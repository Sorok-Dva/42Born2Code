int	ft_iterative_factorial(int nb)
{
	int r;
	int i;

	r = 1;
	i = 1;
	if (nb == 0)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	while ((nb + 1) != i)
	{
		r = r * i;
		i++;
	}
	return (r);
}
